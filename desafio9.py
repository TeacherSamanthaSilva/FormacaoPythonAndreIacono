#Crie um script que leia dois números e mostra a soma, a subtração, a multiplicação e a divisão desses números

num1 = int(input("Digite um número qualquer "))
num2 = int(input("Digite um número qualquer "))

adicao = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print(f"A adicao de {num1} + {num2} é {adicao}")
print(f"A subtracao de {num1} + {num2} é {subtracao}")
print(f"A multiplicacao de {num1} + {num2} é {multiplicacao}")
print(f"A divisao de {num1} + {num2} é {divisao}")
